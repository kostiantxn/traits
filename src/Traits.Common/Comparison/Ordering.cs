namespace Traits.Common.Comparison;

/// <summary>
///     The ordering between two values.
/// </summary>
public enum Ordering
{
    /// <summary>
    ///     The compared value is less than another.
    /// </summary>
    Less,

    /// <summary>
    ///     The compared value is equal to another.
    /// </summary>
    Equal,

    /// <summary>
    ///     The compared value is greater than another.
    /// </summary>
    Greater,
}